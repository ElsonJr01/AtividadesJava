package QuestaoPOO;

public class Casting {
    public static void main(String[] args) {
        float x;
        x = (float)3 / 2;
        System.out.println(x);
        double y;
        y = (float)3 / 2;
        System.out.println(y);
        boolean sim = true;
        sim = !sim;
        System.out.println(sim);
        int inteiro;
        inteiro = (int)99.00929238;
        System.out.println(inteiro);
    }
}
